public class Book 
{
	private String bookTitle = "";
	private int numOfPages = 0;

	public void setBookTitle(String title) 
	{
		bookTitle = title;
	}
	public void setNumOfPages(int numb) 
	{
		numOfPages = numb;
	}

	public String getBookTitle() 
	{
		return bookTitle;
	}
	public int getNumOfPages() 
	{
		return numOfPages;
	}
	


}